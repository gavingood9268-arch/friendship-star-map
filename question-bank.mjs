const shuffle = (items) => {
  const result = [...items];
  for (let index = result.length - 1; index > 0; index -= 1) {
    const target = Math.floor(Math.random() * (index + 1));
    [result[index], result[target]] = [result[target], result[index]];
  }
  return result;
};

const balancedPick = (type, count) => {
  const categories = shuffle([...new Set(QUESTION_BANK.map((question) => question.category))]);
  const groups = new Map(categories.map((category) => [category, shuffle(QUESTION_BANK.filter((question) => question.type === type && question.category === category))]));
  const selected = [];
  while (selected.length < count) {
    for (const category of shuffle(categories)) {
      const question = groups.get(category).pop();
      if (question) selected.push(question);
      if (selected.length === count) break;
    }
  }
  return selected;
};

export const createRoundSet = (rounds = 10) => {
  const pointQuestions = balancedPick('point', Math.ceil(rounds / 2));
  const choiceQuestions = balancedPick('choice', Math.floor(rounds / 2));
  return Array.from({ length: rounds }, (_, index) => {
    const question = index % 2 === 0 ? pointQuestions.shift() : choiceQuestions.shift();
    return { ...question, ...(question.options ? { options: [...question.options] } : {}) };
  });
};
